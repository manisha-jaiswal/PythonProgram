"""Operators in python
Arithmetic operator
Assignment operator
comparision operator
logical operator
identity operator
membership operator
bitwise operator"""

#Arithmetic operator
print("Arithmetic operators")
print("4+2 is",4+2) #addition operator
print("4-2 is",4-2) #substraction operator
print("4*2 is",4*2) #multiplication operator
print("4/2 is",4/2) #division operator
print("4%2 is",4%2) #modulus operator
print("4**2 is",4**2) #exponential operator
print("4//2 is",4//2) #floor division operator

#Assignment Operator - Assigning the value
print("Assignment Operators")
x=5
print(x)
x+=10
print(x)
x-=7
print(x)
x*=2
print(x)
x**=2
print(x)
x/=5
print(x)
x//=5
print(x)
x%=50
print(x)

#comparision operator
print("Comparision operator")
i=5
print(i==5)
print(i==8)
print(i!=5)
print(i>5)
print(i>=5)
print(i<5)
print(i<=5)
print(i==5)

#Logical Operator
print("Logical Operator")
a=True
b=False
print(a and b)
print(a or b)

#identity operator
print("identity operator")
a=True
b=False
print(a is b)
print(a is not b)
print(5 is not 7)
print(5 is 5)

#Membership operator
print("Membership operator")
list1=[3,3,2,5,7,5,4,9,1,6,7,0,2]
print(32 in list1)
print(3 in list1)

#Bitwise operator
print("Bitwise operator")
print(0 & 1)
print(0| 1)
print(0 | 3)
print(1 & 3)